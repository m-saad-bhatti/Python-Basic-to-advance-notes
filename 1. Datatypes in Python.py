# 1. Data Types: int, float, str, bool
number = 10  # int
pi = 3.14159  # float
is_active = True  # bool
message = "Python is awesome!"  # str

print(type(number))  # Prints: <class 'int'>
print(type(pi))  # Prints: <class 'float'>
print(type(is_active))  # Prints: <class 'bool'>
print(type(message))  # Prints: <class 'str'>